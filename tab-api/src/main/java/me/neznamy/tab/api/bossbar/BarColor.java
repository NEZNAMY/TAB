package me.neznamy.tab.api.bossbar;

/**
 * An enum representing all available BossBar colors.
 * Calling ordinal() will return color's network ID.
 */
@SuppressWarnings("unused") // API class
public enum BarColor {

    /** Pink color */
    PINK,

    /** Blue color */
    BLUE,

    /** Red color */
    RED,

    /** Green color */
    GREEN,

    /** Yellow color */
    YELLOW,

    /** Purple color */
    PURPLE,

    /** White color */
    WHITE
}